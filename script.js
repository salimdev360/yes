// User submitted lines
function submitLine(){
  const input=document.getElementById("user-line-input");
  const text=input.value.trim();
  if(text==="") return alert("Type a line first!");
  userLines.push(text);
  const ul=document.getElementById("user-lines-list");
  const li=document.createElement("li");
  li.innerText=text;
  ul.appendChild(li);
  input.value="";
}

// Mashup generator
function generateMashup(){
  const allLines=[...lines.playful,...lines.confident,...lines.smooth,...lines.funny];
  const first=allLines[Math.floor(Math.random()*allLines.length)];
  let second=allLines[Math.floor(Math.random()*allLines.length)];
  while(second===first) second=allLines[Math.floor(Math.random()*allLines.length)];
  document.getElementById("mashup-box").innerText=`${first} / ${second}`;
}

