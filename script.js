// Current mood
let currentMood="playful"; // default

// Set mood
function setMood(mood){
  currentMood=mood;
  document.getElementById("mood-title").innerText=`Your ${mood.charAt(0).toUpperCase()+mood.slice(1)} Line:`;
  generateLine();
}

// Generate line
function generateLine(){
  const moodLines=lines[currentMood];
  if(!moodLines || moodLines.length===0) return;
  const randomLine=moodLines[Math.floor(Math.random()*moodLines.length)];
  const lineBox=document.getElementById("line-box");
  lineBox.innerText=randomLine;

  // Track popularity
  popularLines[randomLine]=popularLines[randomLine]?popularLines[randomLine]+1:1;
  updatePopular();
}

// Copy line
function copyLine(element){
  const text=element.innerText;
  navigator.clipboard.writeText(text);
  element.innerText="Copied ðŸ˜Ž";
  setTimeout(()=>{ element.innerText=text },900);

  // Track popularity
  popularLines[text]=popularLines[text]?popularLines[text]+1:1;
  updatePopular();
}

// Update popular lines
function updatePopular(){
  const popular=Object.entries(popularLines).sort((a,b)=>b[1]-a[1]).slice(0,5);
  const ul=document.getElementById("popular-lines");
  ul.innerHTML="";
  popular.forEach(([line,count])=>{
    const li=document.createElement("li");
    li.innerText=`${line} (${count} copies)`;
    ul.appendChild(li);
  });
}

